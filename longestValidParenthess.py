def isValid(s):
    stack, match = [], {')': '('}
    for ch in s:
        if ch in match:
            if not (stack and stack.pop() == match[ch]):
                return False
        else:
            stack.append(ch)
    return not stack


def solution_use_stack(s):
    stack = [(-1, ')')]
    length = 0
    for i, val in enumerate(s):
        if val == ')' and stack[-1][1] == '(':
            stack.pop()
            length = max(length, i - stack[-1][0])
        else:
            stack.append((i, val))
    return length
